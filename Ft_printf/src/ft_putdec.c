#include "ft_printf.h"

int	ft_putdec(int n)
{
	int	m;
	int	p;

	m = 0;
	if (n == -2147483648)
		return (ft_putstr("-2147483648"));
	if (n < 0)
	{
		m = ft_putchar('-');
		n = -n;
	}
	p = ft_putdecu(n);
	if (p == -1 || m == -1)
		return (-1);
	return (p + m);
}
